## Operadores matemáticos

# Operações basicas

1 + 1
2 - 1
1 * 1
1 / 1

# Divisão com resultado aproximado (sempre pra baixo))

5 / 3 # = 1.6
5 // 3 # = 1
-5 / 3 # = -1.6
-5 // 3 # = -2

# Operação módulo

17 % 5 # = 2
-17 % 5 # = -2

# Exponenciação

4 ** 3 # = 64

# Priorização de parenteses

(3 * 4) + 34 # = 46
